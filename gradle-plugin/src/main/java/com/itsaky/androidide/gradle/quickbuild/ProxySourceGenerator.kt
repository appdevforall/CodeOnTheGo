package com.itsaky.androidide.gradle.quickbuild

/**
 * Emits the Java source of a generated proxy activity: a subclass of the user's activity
 * whose only member is the app-switcher gesture hook (plan A3). Extension (not
 * delegation) is enough because proxy and user class both travel in the payload dex - a
 * reload swaps the whole hierarchy together (see quick-build/README.md, "Test-app
 * architecture").
 */
object ProxySourceGenerator {
	/** Runtime gesture hook the proxies call; public entry point of the runtime AAR. */
	private const val GESTURES_CLASS = "com.itsaky.androidide.quickbuild.runtime.QuickBuildGestures"

	/**
	 * @param proxyClass fully-qualified proxy class name (must contain a package).
	 * @param userClass fully-qualified user activity class the proxy extends.
	 */
	fun generateSource(
		proxyClass: String,
		userClass: String,
	): String {
		require('.' in proxyClass) { "proxy class '$proxyClass' has no package" }
		val packageName = proxyClass.substringBeforeLast('.')
		val simpleName = proxyClass.substringAfterLast('.')
		return buildString {
			append("package ").append(packageName).append(";\n")
			append('\n')
			append("/**\n")
			append(" * Generated by CoGo Quick Build (ADFA-4128). Gives the manifest a stable component\n")
			append(" * name while the user's activity class stays swappable: proxy and superclass both\n")
			append(" * travel in the payload dex, so a hot reload swaps them together.\n")
			append(" */\n")
			append("public class ")
				.append(simpleName)
				.append(" extends ")
				.append(userClass)
				.append(" {\n")
			append('\n')
			append("\t/**\n")
			append("\t * Quick Build app-switcher gesture (3-finger tap returns to CoGo). The hook only\n")
			append("\t * observes; every event still reaches the app via super, unconsumed and undelayed.\n")
			append("\t */\n")
			append("\t@Override\n")
			append("\tpublic boolean dispatchTouchEvent(android.view.MotionEvent ev) {\n")
			append("\t\t").append(GESTURES_CLASS).append(".onDispatchTouchEvent(this, ev);\n")
			append("\t\treturn super.dispatchTouchEvent(ev);\n")
			append("\t}\n")
			append("}\n")
		}
	}
}
